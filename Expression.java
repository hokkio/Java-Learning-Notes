
public class Expression {

	public static void main(String[] args) {
		
		double friends = 10;
		
		friends = friends - 1;
		friends = friends + 1;
		friends--; // Same with -1
		friends++; // Same with +1
		friends = friends / 2;
		friends = friends * 2;
		friends = friends / 3; // Need to change int into double
		
		
		System.out.println(friends);

	}

}
